 <h2>	Quem Somos?<br><br></h2>

Os empresários Diego e Douglas sempre foram apaixonados por doces, no ano de  2012 resolveram transformar essa paixão em negócio. Assim, foi criado  os Doces Caseiros, uma loja especializada em Doces gourmet no pote.<br>

<p>Entre suas inúmeras criações,  destaque-se o  carro chefe da marca, o pote sabor  Cura TPM. Recheado com creme de Leite Ninho,Nutella e Morango silvestres. Ele é sucesso entre as mulheres.</p>

<p>Com o passar dos anos e com mais experiência no mercado perceberam a exigência em ampliar sua gama de produtos, hoje contam uma linha especial de 5 tipos  doces gourmet de pote.</p>

<p>Os produtos são 100% artesanais, elaborados com ingredientes selecionados e de alta qualidade, que deixam tudo com um sabor único e inexplicável.</p>

<p>Para você que é lojista, experimente ter os nossos produtos em suas prateleiras e colha os benefícios das vendas e do sucesso que temos no mercado!</p>
